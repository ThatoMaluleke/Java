    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
    int Num1 = Integer.parseInt(txtInput1.getText());
    int Num2 = Integer.parseInt(txtInput2.getText());
    int result = 0;
    String Op = cboOp.getSelectedItem().toString();

    if (Op.equals("Add")) {
        result = Num1 + Num2;
        lblOutput.setText(Num1 + " + " + Num2 + " = " + result);
    }
    else if (Op.equals("Subtract")) {
        result = Num1 - Num2;
        lblOutput.setText(Num1 + " - " + Num2 + " = " + result);
    }
    else if (Op.equals("Multiply")) {
        result = Num1 * Num2;
        lblOutput.setText(Num1 + " √ó " + Num2 + " = " + result);
    }
    else if (Op.equals("Divide")) {
        if (Num2 == 0) {
            // Division by zero detected
            JOptionPane.showMessageDialog(null, "Division by zero is not allowed! üòÖ");
            lblOutput.setText("Cannot divide by zero!");
        } else {
            result = Num1 / Num2;
            lblOutput.setText(Num1 + " √∑ " + Num2 + " = " + result);
        }
    }
    else {
        lblOutput.setText("Invalid Operation");
    }

} catch (NumberFormatException e) {
    // Handles invalid input like letters
    lblOutput.setText("Please enter valid numbers! üö´");
} catch (ArithmeticException e) {
    // Extra safety for arithmetic errors
    lblOutput.setText("Arithmetic error occurred! ‚ö†Ô∏è");
} catch (Exception e) {
    // Catches unexpected exceptions
    lblOutput.setText("Something went wrong...");
    e.printStackTrace();
}

       
    }//GEN-LAST:event_jButton1ActionPerformed

